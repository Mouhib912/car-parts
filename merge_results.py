"""
merge_results.py
Merges multiple Excel files generated by batch processing into a single final Excel file.
"""

import os
import pandas as pd
from openpyxl import load_workbook, Workbook
from openpyxl.drawing.image import Image as XLImage
from openpyxl.styles import Font, PatternFill, Alignment, Border, Side
from io import BytesIO
import re

def extract_indices(filename):
    """Extract start and end indices from filename like results_batch_0_200.xlsx"""
    match = re.search(r"results_batch_(\d+)_(\d+)\.xlsx", filename)
    if match:
        return int(match.group(1)), int(match.group(2))
    return float('inf'), float('inf')

def merge_excels(input_dir, output_file):
    files = [f for f in os.listdir(input_dir) if f.startswith("results_batch_") and f.endswith(".xlsx")]
    # Sort files by start index to maintain order
    files.sort(key=extract_indices)
    
    if not files:
        print("No result files found to merge.")
        return

    print(f"Found {len(files)} files to merge.")
    
    final_wb = Workbook()
    final_ws = final_wb.active
    final_ws.title = "Car Parts"

    # Define styles (same as in the scraper script)
    header_font = Font(name="Arial", bold=True, color="FFFFFF", size=11)
    header_fill = PatternFill("solid", start_color="1F4E79")
    center      = Alignment(horizontal="center", vertical="center", wrap_text=True)
    left_wrap   = Alignment(horizontal="left",   vertical="center", wrap_text=True)
    thin        = Border(
        left=Side(style="thin"),  right=Side(style="thin"),
        top=Side(style="thin"),   bottom=Side(style="thin"),
    )
    alt_fill = PatternFill("solid", start_color="D9E1F2")

    headers    = ["#", "Code", "Part Name", "Brand", "Model", "Category", "Image", "Status"]
    col_widths = [4,    15,     35,           15,      20,      22,          24,      10]

    # Header row
    for col, (h, w) in enumerate(zip(headers, col_widths), 1):
        cell           = final_ws.cell(row=1, column=col, value=h)
        cell.font      = header_font
        cell.fill      = header_fill
        cell.alignment = center
        cell.border    = thin
        final_ws.column_dimensions[cell.column_letter].width = w
    final_ws.row_dimensions[1].height = 30

    current_row = 2
    total_images = 0

    for file in files:
        print(f"Processing {file}...")
        wb = load_workbook(os.path.join(input_dir, file))
        ws = wb.active
        
        # Get images from the source sheet
        images_dict = {}
        for image in ws._images:
            # image.anchor._from.row is 0-indexed
            images_dict[image.anchor._from.row + 1] = image

        # Iterate through data rows (skipping header)
        for row in range(2, ws.max_row + 1):
            final_ws.row_dimensions[current_row].height = 115
            fill = alt_fill if (current_row - 1) % 2 == 0 else None
            
            # Copy values and styles
            for col in range(1, ws.max_row + 1):
                if col > 8: break # Only 8 columns
                
                val = ws.cell(row=row, column=col).value
                # Update the '#' column to be continuous
                if col == 1:
                    val = current_row - 1
                
                new_cell = final_ws.cell(row=current_row, column=col, value=val)
                new_cell.alignment = left_wrap if col in (3, 4, 5, 6) else center
                new_cell.border = thin
                if fill:
                    new_cell.fill = fill
            
            # Copy image if exists
            if row in images_dict:
                img = images_dict[row]
                # Re-anchor the image to the new row by regenerating it from byte data
                try:
                    new_img = XLImage(BytesIO(img._data()))
                    new_img.width = img.width
                    new_img.height = img.height
                    final_ws.add_image(new_img, f"G{current_row}")
                    total_images += 1
                except Exception as e:
                    print(f"Warning: Failed to copy image at row {row}: {e}")
            
            current_row += 1
        
        wb.close()

    final_wb.save(output_file)
    print(f"\nMerged {len(files)} files into {output_file}")
    print(f"Total rows: {current_row - 2}")
    print(f"Total images: {total_images}")

if __name__ == "__main__":
    import argparse
    parser = argparse.ArgumentParser(description="Merge batch results")
    parser.add_argument("--input-dir", type=str, default=".", help="Directory containing batch results")
    parser.add_argument("--output", type=str, default="final_results_merged.xlsx", help="Output filename")
    args = parser.parse_args()
    
    merge_excels(args.input_dir, args.output)
